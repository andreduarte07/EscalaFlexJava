    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtUnidade.getText();
        String telefone = txtTelefone.getText();
        String cep = txtCEP.getText();
        String responsavel = txtResponsavel.getText();
        String endereco = txtEndereco.getText();
        String email = txtEmail.getText();
        Cidade cid = (Cidade) cmbCidades.getSelectedItem();
        Estado est = (Estado) cmbEstados.getSelectedItem();
        String erro = "";
        if ( nome.isEmpty() ){
            erro += "Nome\n";
        }
        if ( telefone.isEmpty() ){
            erro += "Telefone\n";
        }
        if ( cep.isEmpty() ){
            erro += "CEP\n";
        }
        if ( responsavel.isEmpty() ){
            erro += "Responsavel\n";
        }
        if ( endereco.isEmpty() ){
            erro += "Endereco\n";
        }
        if ( email.isEmpty() ){
            erro += "Email\n";
        }
        if ( cid.getId() == 0 ){
            erro += "Cidade\n";
        }
        if ( est.getId() == 0 ){
            erro += "Estado\n";
        }
        
        if ( !erro.isEmpty() ){
            JOptionPane.showMessageDialog(this, "Voc√™ esqueceu de preencher os seguintes campos:\n"
                                                + erro );
        }else{
 
                Unidade uni = new Unidade();
                uni.setNome(nome);
                uni.setEndereco(endereco);
                uni.setTelefone(telefone);
                uni.setCep(cep);
                uni.setEmail(email);
                uni.setResponsavel(responsavel);
                uni.setCidade(cid);
                uni.setEstado(est);
                UnidadeDAO.inserir(uni);
        }            
            limpar();
            
        
              
    }//GEN-LAST:event_btnSalvarActionPerformed
    private void limpar(){
        txtUnidade.setText("");
        txtCEP.setText("");
        txtEmail.setText("");
        txtResponsavel.setText("");
        txtTelefone.setText("");
        txtEndereco.setText("");
        cmbCidades.setSelectedIndex(0);
        cmbEstados.setSelectedIndex(0);

    }
    

